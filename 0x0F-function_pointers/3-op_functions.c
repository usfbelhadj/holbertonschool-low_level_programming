#include "3-calc.h"
/**
 * op_add - sums
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - substracts
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplies
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divises
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - rest int
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_mod(int a, int b)
{
return (a % b);
}
